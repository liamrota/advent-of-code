function getTotalScore(input) {
  let score = 0;
  const scoreObj = {
    X: {
      score: 1,
      scoreAgainstOpp: {
        A: 3,
        B: 0,
        C: 6
      }
    },
    Y: {
      score: 2,
      scoreAgainstOpp: {
        A: 6,
        B: 3,
        C: 0
      }
    },
    Z: {
      score: 3,
      scoreAgainstOpp: {
        A: 0,
        B: 6,
        C: 3
      }
    }
  };

  input.split('\n').forEach(x => {
    const oppChoice = x[0];
    const yourChoice = x[2];
    score += scoreObj[yourChoice].score;
    score += scoreObj[yourChoice].scoreAgainstOpp[oppChoice];
  });
  return score;
}

